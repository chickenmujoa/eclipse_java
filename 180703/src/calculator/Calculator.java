package calculator;

public class Calculator {
	
	public int add(int inputNum1, int inputNum2) {
		return inputNum1 + inputNum2;
	}
	
	public int subtract(int inputNum1, int inputNum2) {
		return inputNum1 - inputNum2;
	}
	
	public int multiply(int inputNum1, int inputNum2) {
		return inputNum1 * inputNum2;
	}
	
	public int divide(int inputNum1, int inputNum2) {
		return inputNum1 / inputNum2;
	}
}
